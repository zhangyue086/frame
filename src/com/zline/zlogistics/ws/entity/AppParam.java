package com.zline.zlogistics.ws.entity;

/**
 * Description: 接口 参数实体

 * @author zhangzhitao

 * @date 2015-6-23

 * @version 1.0

 */
public class AppParam {
	
	private String md5;
	private String mobile;
	private String data;
	public String getMd5() {
		return md5;
	}
	public void setMd5(String md5) {
		this.md5 = md5;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
}
